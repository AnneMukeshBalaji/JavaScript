// ! Arithmetic Operators are the Mathematical Operators 

let num1 = 10
let num2 = 5
console.log(`Operator + :${num1+num2}`)
console.log(`Operator - : ${num1-num2}`)
console.log(`Operator * : ${num1*num2}`)
console.log(`Operator / : ${num1/num2}`)
console.log(`Operator % : ${num1%num2}`)
console.log(`Operator ** : ${num1**num2}`)



// ! This Are also known as increment and decrement operators 

console.log(`Operator ++ : ${num1++}`) // * This will print the value and then increment
console.log(`Operator -- : ${num2--}`) // * This will print the value and then decerement 
console.log(num1) // * 11
console.log(num2) // * 4
console.log(`--num1 : ${    --num1}`)
console.log(`++num2 : ${++num2}`)